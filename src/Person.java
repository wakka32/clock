public class Person {
    private String name;
    private String adress;

    public Person(String N, String A) {
        this.name = N;
        this.adress = A;
    }

    public String getName() {
        return name;
    }

    public String getAdress() {
        return adress;
    }

    public void setAdress(String N) {
        this.adress = N;
    }

}


